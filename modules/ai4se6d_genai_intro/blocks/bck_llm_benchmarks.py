"""Slides — AI Intelligence Index benchmarks (4 views) with hover tooltip."""
# @guideline: maximize-viewport
from streamtex import *
from streamtex.bib import cite
from streamtex.enums import Tags as t
from custom.styles import Styles as s
from shared_widgets import st_hover_tooltip


class BlockStyles:
    """Benchmark slides styles."""
    heading = s.project.titles.slide_title + s.center_txt
    source = s.project.citation + s.large + s.center_txt
bs = BlockStyles


# (title, image filename, has_tooltip)
_BENCHMARKS = [
    ("AI Index: Results", "ai-index-intelligence.png", False),
    ("AI Index: Token Usage", "ai-index-tokens.webp", True),
    ("AI Index: Cost Breakdown", "ai-index-cost.png", False),
    ("AI Index: Score vs. Release Date", "ai-index-releasedate.png", False),
]

# Token glossary entries
_TOKEN_ENTRIES = [
    (
        "Input tokens",
        "Cumulative tokens sent to the model at each turn of the agentic loop "
        "(initial prompts + re-injected conversation history + tool results + "
        "summarized context), across all tasks and repetitions.",
    ),
    (
        "Reasoning tokens",
        "Internal thinking tokens generated by reasoning models "
        "(e.g. o3 chain-of-thought, Claude extended thinking), "
        "not visible in the final response.",
    ),
    (
        "Output tokens",
        "Visible response tokens produced by the model "
        "(final text + emitted tool calls).",
    ),
]


def build():
    for idx, (title, filename, has_tooltip) in enumerate(_BENCHMARKS):
        with st_block(s.project.containers.page_fill_top):
            with st_block(s.center_txt):
                lvl = "1" if idx == 0 else "2"
                st_space("v", "-30vh")
                st_write(bs.heading, title, tag=t.div, toc_lvl=lvl)

                if has_tooltip:
                    st_hover_tooltip(
                        title="Token Types",
                        entries=_TOKEN_ENTRIES,
                        scale="2vw",
                        width="65vw",
                        position="center",
                    )

                st_image(
                    s.none,
                    uri=f"_SHARED/images/{filename}",
                    width="100%",
                )
                st_write(bs.source, cite("artificialanalysis2026"))

        if idx < len(_BENCHMARKS) - 1:
            st_slide_break()
